package com.revature;

/**
 * Naming Conventions
 * ---------------------
 * classes - PascalCase 
 * fields/variables - camelCase
 * packages - reverse domain-name, lowercase
 * interfaces - same as classes
 */
public class Conventions {
	public static void main(String[] args) {
		// Variable declaration starts with data type, then variable name
		// Strings use ""
		// chars use '' (primitive single character)
		String x = "Hello World";
	}
}
